package com.kian.perficon.ui.components

import androidx.compose.ui.geometry.Offset
import androidx.compose.ui.geometry.Size
import androidx.compose.ui.graphics.Color
import androidx.compose.ui.graphics.painter.Painter

/**
 * A Painter that renders pixel art from a character grid.
 * '#' pixels are drawn in [pixelColor]; '.' are transparent.
 * When used with Icon(), the tint is applied automatically via saveLayer + ColorFilter.
 */
class PixelArtPainter(
    private val grid: List<String>,
    private val pixelColor: Color = Color.Black
) : Painter() {
    private val rows = grid.size
    private val cols = grid.maxOfOrNull { it.length } ?: 1

    override val intrinsicSize: Size = let {
        val maxDim = maxOf(rows, cols).toFloat()
        if (maxDim > 0) {
            val scale = 24f / maxDim
            Size(cols * scale, rows * scale)
        } else {
            Size(24f, 24f)
        }
    }

    override fun androidx.compose.ui.graphics.drawscope.DrawScope.onDraw() {
        val pixelW = size.width / cols
        val pixelH = size.height / rows
        for (r in 0 until rows) {
            val row = grid[r]
            for (c in row.indices) {
                if (row[c] == '#') {
                    drawRect(
                        color = pixelColor,
                        topLeft = Offset(c * pixelW, r * pixelH),
                        size = Size(pixelW + 0.5f, pixelH + 0.5f)
                    )
                }
            }
        }
    }
}

